def Get(_url):
    headers = {
        "user-agent":random.choice(UserAgentList)
    }
    GET = requests.get(_url, headers=headers)
    if GET.status_code == 200:
        return GET
    else:
        return False

def GetRanking():
    Ranking = []
    page = random.randint(1, 5)
    GET = Get(f"https://www.vilipix.com/ranking?mode=monthly&p={page}")
    if GET:
        ul = BeautifulSoup(GET.text, "lxml").find("ul", class_="illust-content").find_all("li")
        for li in ul:
            Ranking.append("https://www.vilipix.com"+li.a.attrs['href'])
        return Ranking
    else:
        return False

def GetImageUrl(_url):
    GET = Get(_url)
    if GET:
        ImgUrl = BeautifulSoup(GET.text, "lxml").find("ul", class_="illust-pages").a.img.attrs['src']
        return ImgUrl
    else:
        return False